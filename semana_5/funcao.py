def soma(x, y):  # funcao soma. Para criarmos essa funcao, usamos def
    return x + y  # retorna soma de x + y . Que nesse caso sao dois parâmetros


def multiplica(x, y, z):  # funcao multiplica. Nesse caso há três parâmetros
    return x * y * z


print(soma(10, 20))
print(multiplica(2, 100, 2))
